/* ============================================================
   CASE‑INSENSITIVE LOOKUP FUNCTION
   ------------------------------------------------------------
   - Converts user input to lowercase
   - Finds exact match (O‑1)
   - Supports synonyms (S‑1)
   - Supports plural forms (P‑1)
   - Returns null if no match
   ============================================================ */

function getTranslationForItem(name) {
  if (!name) return null;

  const key = name.trim().toLowerCase();

  // Build a lowercase → original key map once
  if (!window._FOOD_KEY_MAP) {
    window._FOOD_KEY_MAP = {};
    for (const originalKey in FOOD_TRANSLATIONS) {
      window._FOOD_KEY_MAP[originalKey.toLowerCase()] = originalKey;
    }
  }

  const matchedKey = window._FOOD_KEY_MAP[key];
  if (!matchedKey) return null;

  return FOOD_TRANSLATIONS[matchedKey];
}



